    public function index()
    {
        // Check user is student
        if (auth()->user()->user_type != 'student') {
            return redirect('/')->with('error', 'Your are not allowed to see this page');
        }
        $appointments = Appointment::where('student_id', auth()->user()->id)->orderBy('created_at', 'DESC')->orderBy('request_status', 'ASC')->paginate(10);
        return view('student.appointments.list', [
            'appointments' => $appointments
        ]);
    }

